#ifndef _S390_CURRENT_H
#define _S390_CURRENT_H

#ifdef __KERNEL__

struct task_struct;

static inline struct task_struct * get_current(void)
{
        struct task_struct *current;
        __asm__("lghi  %0,-16384\n\t"
                "alg   %0,0xd40"
                : "=&r" (current) : : "cc" );
        return current;
 }

#define current get_current()

#endif

#endif /* !(_S390_CURRENT_H) */
